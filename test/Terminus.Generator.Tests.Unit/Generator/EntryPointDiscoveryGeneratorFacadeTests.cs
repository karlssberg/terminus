        {
            TestState =
            {
                Sources = { source }
            }
        };
        
        string expectedMainSource =
            """
            // <auto-generated/> Generated by Terminus EntryPointDiscoveryGenerator
            #nullable enable

            namespace Demo
            {
                public partial interface IFacade
                {
                    global::System.Collections.Generic.IAsyncEnumerable<int> Stream();
