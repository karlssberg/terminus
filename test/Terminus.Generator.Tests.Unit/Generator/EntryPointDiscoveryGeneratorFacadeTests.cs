    [Fact]
    public async Task Given_a_single_parameter_method_Should_generate_entry_point_command()
    {
        const string source =
            """
            using System;
            using Terminus;
            using System.Threading;

            namespace Demo
            {
                [EntryPointFacade(typeof(EntryPointAttribute), Scoped=true)]
                public partial interface IFacade;
                
                public class EntryPointAttribute : Attribute;

                public static class TestEntryPoints
                {
                    [EntryPoint]
                    public static void Hello(string world, CancellationToken cancellationToken)
                    {
                        Console.WriteLine($"hello {world}");
                    }
                }
            }
            """;

        const string expectedMainSource =
            """
            // <auto-generated/> Generated by Terminus EntryPointDiscoveryGenerator
            #nullable enable

            namespace Demo
            {
                public partial interface IFacade
                {
                    void Hello(string world, global::System.Threading.CancellationToken cancellationToken);
                }

                internal sealed class IFacade_Generated : global::Demo.IFacade
                {
                    private readonly global::System.IServiceProvider _serviceProvider;
                    private readonly global::Terminus.Dispatcher<global::Demo.IFacade> _dispatcher;
                    public IFacade_Generated(global::System.IServiceProvider serviceProvider, global::Terminus.Dispatcher<global::Demo.IFacade> dispatcher)
                    {
                        _serviceProvider = serviceProvider;
                        _dispatcher = dispatcher;
                    }

                    public void Hello(string world, global::System.Threading.CancellationToken cancellationToken)
                    {
                        cancellationToken.ThrowIfCancellationRequested();
                        global::Demo.TestEntryPoints.Hello(world, cancellationToken);
                    }
                }
            }
            """;

    var test = new TerminusSourceGeneratorTest<EntryPointDiscoveryGenerator>
        {
            TestState =
            {
                Sources = { source }
            }
        };

        test.TestState.GeneratedSources.Add((
            typeof(EntryPointDiscoveryGenerator),
            "Demo_IFacade_Generated.g.cs",
            SourceText.From(expectedMainSource, Encoding.UTF8)));

        await test.RunAsync();
    }

